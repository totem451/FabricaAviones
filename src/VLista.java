    private String[] generarLista(){
        int len = fabrica.getLista().length;
        String[] resultado = new String[len];
        for(int i = 0; i < len; i++){
            resultado[i] =  String.format("Id %d: %s Peso Total = %d kg", fabrica.getLista()[i].getId(), fabrica.getLista()[i].toString(), fabrica.getLista()[i].pesoTotal());
//                    String.format("%d: peso total:%d kg", fabrica.getLista()[i].getId(), fabrica.getLista()[i].pesoTotal());
        }
        return resultado;
    }


