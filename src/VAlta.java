    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(jRadioButton1.isSelected()){
            int id = this.fabrica.getLista().length;
            int gtce = Integer.parseInt(jTextField4.getText());
            int gtcv = Integer.parseInt(jTextField1.getText());
            Apasajeros ap = new Apasajeros(gtce, gtcv, id);
            fabrica.darAlta(ap);
        }
        if(jRadioButton2.isSelected()){
            int id = this.fabrica.getLista().length;
            int gtc = Integer.parseInt(jTextField2.getText());
            Acarga ac = new Acarga(gtc, id);
            fabrica.darAlta(ac);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

